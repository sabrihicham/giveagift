import 'dart:ui';

class ColorsResponse {
  ColorsResponse({
    required this.status,
    this.message,
    required this.data,
  });

  final String status;
  final String? message;
  final ColorsData data;

  factory ColorsResponse.fromJson(Map<String, dynamic> json) => ColorsResponse(
        status: json["status"],
        message: json["message"],
        data: ColorsData.fromJson(json["data"]),
      );

  Map<String, dynamic> toJson() => {
        "status": status,
        "message": message,
        "data": data.toJson(),
      };
}

class ColorsData {
  ColorsData({
    required this.colors,
    required this.proColors,
  });

  final List<ColorModel> colors;
  final List<ProColor> proColors;

  factory ColorsData.fromJson(Map<String, dynamic> json) => ColorsData(
        colors: List<ColorModel>.from(
            json["colors"].map((x) => ColorModel.fromJson(x))),
        proColors: List<ProColor>.from(
            json["proColors"].map((x) => ProColor.fromJson(x))),
      );

  Map<String, dynamic> toJson() => {
        "colors": List<dynamic>.from(colors.map((x) => x.toJson())),
        "proColors": List<dynamic>.from(proColors.map((x) => x.toJson())),
      };
}

class MyColor {
  String? id;
  MyColor({this.id});
}

class ColorModel extends MyColor {
  ColorModel({
    super.id,
    required this.hex,
  });

  final String hex;

  factory ColorModel.fromJson(Map<String, dynamic> json) => ColorModel(
    id: json["_id"],
    hex: json["hex"],
  );

  Map<String, dynamic> toJson() => {
    "_id": id,
    "hex": hex,
  };

  // #A569BD -> 0xffA569BD
  Color get color => Color(int.parse('0xff$hex'.replaceAll(RegExp('#'), '')));
}

class ProColor extends MyColor {
  ProColor({
    super.id,
    required this.image,
    this.price,
  });

  final String image;
  final int? price;

  factory ProColor.fromJson(Map<String, dynamic> json) => ProColor(
    id: json["_id"],
    image: json["image"],
    price: json["price"],
  );

  Map<String, dynamic> toJson() => {
    "_id": id,
    "image": image,
    "price": price,
  };
}
