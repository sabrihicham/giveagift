class ShapesResponse {
  ShapesResponse({
    required this.status,
    required this.shaps,
  });

  final String status;
  final List<Shape> shaps;

  factory ShapesResponse.fromJson(Map<String, dynamic> json) => ShapesResponse(
        status: json["status"],
        shaps: List<Shape>.from(json["data"].map((x) => Shape.fromJson(x))),
      );

  Map<String, dynamic> toJson() => {
        "status": status,
        "data": List<dynamic>.from(shaps.map((x) => x.toJson())),
      };
}

class Shape {
  Shape({
    required this.id,
    required this.image,
    required this.price,
    this.priority,
  });

  final String id;
  final String image;
  final num? price, priority;

  factory Shape.fromJson(Map<String, dynamic> json) => Shape(
    id: json["_id"],
    image: json["image"],
    price: json["price"],
    priority: json["priority"],
  );

  Map<String, dynamic> toJson() => {
    "_id": id,
    "image": image,
    "price": price,
    "priority": priority,
  };
}